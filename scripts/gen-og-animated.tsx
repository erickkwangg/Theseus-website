/**
 * Generates the canonical animated Open Graph image for the Theseus homepage.
 *
 * Output: public/og/root.gif (1200x630, looping, cream hero card with the
 * exact typewriter cycle from src/components/Pages/Home/Hero.tsx —
 * "Verified." → "Autonomous." → "Sovereign." at 85 ms type / 40 ms delete /
 * 1800 ms hold). Frame 1 is the static fallback for Twitter/X, LinkedIn,
 * Facebook, iMessage, WhatsApp; Telegram, Discord, and Slack render the loop.
 *
 * Run when the brand line, headline copy, or visual treatment changes:
 *
 *   npm run gen:og
 *
 * Then commit the updated public/og/root.gif. This file is not regenerated by
 * the Next.js build; per-segment OGs at /docs, /launch, /playground are still
 * generated dynamically by Next.js via their respective opengraph-image.tsx files.
 */

import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

import React from "react";
import satori, { type SatoriOptions } from "satori";
import { Resvg } from "@resvg/resvg-js";
import GIFEncoder from "gif-encoder-2";
import sharp from "sharp";

void React;

const WIDTH = 1200;
const HEIGHT = 630;

const ROTATING_WORDS = ["Verified.", "Autonomous.", "Sovereign."] as const;
const TYPE_MS = 85;
const DELETE_MS = 40;
const HOLD_MS = 1800;

const FONT_URLS = {
  serifRegular:
    "https://raw.githubusercontent.com/google/fonts/main/ofl/instrumentserif/InstrumentSerif-Regular.ttf",
  serifItalic:
    "https://raw.githubusercontent.com/google/fonts/main/ofl/instrumentserif/InstrumentSerif-Italic.ttf",
  sansRegular:
    "https://raw.githubusercontent.com/vercel/geist-font/main/packages/next/dist/fonts/geist-sans/Geist-Regular.ttf",
  sansSemiBold:
    "https://raw.githubusercontent.com/vercel/geist-font/main/packages/next/dist/fonts/geist-sans/Geist-SemiBold.ttf",
  monoRegular:
    "https://raw.githubusercontent.com/vercel/geist-font/main/packages/next/dist/fonts/geist-mono/GeistMono-Regular.ttf",
} as const;

async function fetchFont(url: string): Promise<ArrayBuffer> {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`Font fetch failed (${res.status}): ${url}`);
  return res.arrayBuffer();
}

const LOGO_RENDER_PX = 96;

/**
 * Renders src/assets/logo.svg as a pure black silhouette PNG (data URL).
 *
 * The website applies `filter: brightness(0)` in CSS to render the same
 * Theseus mark in solid black. Satori does not support CSS filters, so we
 * pre-render the silhouette here: rasterize the SVG with resvg, then zero
 * the RGB channels of every opaque pixel (preserving alpha).
 */
async function loadBlackLogoDataUrl(): Promise<string> {
  const svgPath = resolve(
    dirname(fileURLToPath(import.meta.url)),
    "..",
    "src",
    "assets",
    "logo.svg",
  );
  const svgBuffer = await readFile(svgPath);

  const rendered = new Resvg(svgBuffer, {
    fitTo: { mode: "width", value: LOGO_RENDER_PX },
  }).render();

  const { width, height, pixels } = rendered;
  const rgba = Buffer.from(pixels);
  for (let i = 0; i < rgba.length; i += 4) {
    if (rgba[i + 3] > 0) {
      rgba[i] = 0;
      rgba[i + 1] = 0;
      rgba[i + 2] = 0;
    }
  }

  const png = await sharp(rgba, {
    raw: { width, height, channels: 4 },
  })
    .png()
    .toBuffer();

  return `data:image/png;base64,${png.toString("base64")}`;
}

