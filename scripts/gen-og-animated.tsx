type FrameSpec = { typed: string; delayMs: number };

/**
 * Build the (typed, delay) sequence that mirrors Hero.tsx's typewriter loop.
 *
 * The animation samples every other character during type/delete and doubles
 * the per-frame delay so the perceived character cadence stays the same as the
 * 85 ms / 40 ms hero. This halves the GIF frame count without changing the
 * total cycle duration.
 */
function buildSequence(): FrameSpec[] {
  const seq: FrameSpec[] = [];

  seq.push({ typed: ROTATING_WORDS[0], delayMs: HOLD_MS });

  for (let i = 0; i < ROTATING_WORDS.length; i++) {
    const word = ROTATING_WORDS[i];
    const next = ROTATING_WORDS[(i + 1) % ROTATING_WORDS.length];

    for (let n = word.length - 2; n >= 0; n -= 2) {
      seq.push({ typed: word.slice(0, n), delayMs: DELETE_MS * 2 });
    }
    if (word.length % 2 === 1) {
      seq.push({ typed: "", delayMs: DELETE_MS * 2 });
    }
    for (let n = 2; n < next.length; n += 2) {
      seq.push({ typed: next.slice(0, n), delayMs: TYPE_MS * 2 });
    }
    seq.push({ typed: next, delayMs: TYPE_MS * 2 });
    if (i < ROTATING_WORDS.length - 1) {
      seq.push({ typed: next, delayMs: HOLD_MS });
    }
  }

  return seq;
}

async function renderFrame(
  typed: string,
  fonts: SatoriOptions["fonts"],
  logoSrc: string,
) {
  const svg = await satori(frame(typed, logoSrc), {
    width: WIDTH,
    height: HEIGHT,
    fonts,
  });
  return new Resvg(svg, { fitTo: { mode: "width", value: WIDTH } })
    .render()
    .pixels;
}

async function main() {
  process.stdout.write("Loading fonts and logo...\n");
  const [serifRegular, serifItalic, sansSemiBold, sansRegular, monoRegular, logoSrc] =
    await Promise.all([
      fetchFont(FONT_URLS.serifRegular),
      fetchFont(FONT_URLS.serifItalic),
      fetchFont(FONT_URLS.sansSemiBold),
      fetchFont(FONT_URLS.sansRegular),
      fetchFont(FONT_URLS.monoRegular),
      loadBlackLogoDataUrl(),
    ]);

  const fonts: SatoriOptions["fonts"] = [
    { name: "Geist", data: sansRegular, weight: 400, style: "normal" },
    { name: "Geist", data: sansSemiBold, weight: 600, style: "normal" },
    { name: "Instrument Serif", data: serifRegular, weight: 400, style: "normal" },
    { name: "Instrument Serif", data: serifItalic, weight: 400, style: "italic" },
    { name: "Geist Mono", data: monoRegular, weight: 400, style: "normal" },
  ];

  const sequence = buildSequence();
  process.stdout.write(`Rendering ${sequence.length} frames...\n`);

  const cache = new Map<string, Uint8Array>();
  const rendered: Uint8Array[] = [];
  for (let i = 0; i < sequence.length; i++) {
    const { typed } = sequence[i];
    let pixels = cache.get(typed);
    if (!pixels) {
      pixels = await renderFrame(typed, fonts, logoSrc);
      cache.set(typed, pixels);
    }
    rendered.push(pixels);
    if ((i + 1) % 10 === 0 || i === sequence.length - 1) {
      process.stdout.write(`  ${i + 1}/${sequence.length}\n`);
    }
  }

  process.stdout.write("Encoding GIF...\n");
  const encoder = new GIFEncoder(WIDTH, HEIGHT, "neuquant", true, sequence.length);
  encoder.setRepeat(0);
  encoder.setQuality(10);
  encoder.start();
  for (let i = 0; i < sequence.length; i++) {
    encoder.setDelay(sequence[i].delayMs);
    encoder.addFrame(rendered[i]);
  }
  encoder.finish();

  const buffer: Buffer = encoder.out.getData();
  const outPath = resolve(
    dirname(fileURLToPath(import.meta.url)),
    "..",
    "public",
    "og",
    "root.gif",
  );
  await mkdir(dirname(outPath), { recursive: true });
  await writeFile(outPath, buffer);

  const sizeKb = (buffer.length / 1024).toFixed(1);
  process.stdout.write(
    `Wrote ${outPath} (${sizeKb} KB, ${sequence.length} frames)\n`,
  );
}

main().catch((err) => {
  process.stderr.write(`gen-og-animated failed: ${err?.stack ?? err}\n`);
  process.exit(1);
});
